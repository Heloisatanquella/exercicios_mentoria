# Exercício 01: Cálculo de Área de um Retângulo

# Solicita as medidas ao usuário

comprimento_retangulo = input('Insira o comprimento do retângulo:')
largura_retangulo = input('Insira a largura do retângulo:')

# Cálculo da área #
area = float(comprimento_retangulo) * float(largura_retangulo)

# Imprime o resultado na tela do usuário
print(area)
